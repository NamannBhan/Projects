import numpy as np
import plotly.graph_objects as go

# ── CORE SETTINGS ───────────────────────────────────
position = 1_000_000
TRADING_DAYS = 252
n_sims = 20000
seed = 42
rng = np.random.default_rng(seed)

# ── MARKET ASSUMPTIONS ─────────────────────────────
S0, theta = 75, 70

# Increased granularity for smoother "evolution"
kappa_vals = np.linspace(0.5, 5.0, 20)
sigma_vals = np.linspace(0.90, 2.00, 20)
T_vals = np.arange(1, 21, 1) # 1 to 20 days

def simulate_state(kappa, sigma, T):
    dt = 1 / TRADING_DAYS
    S = np.full(n_sims, S0, dtype=np.float64)
    for _ in range(int(T)):
        Z = rng.normal(0, 1, n_sims)
        S += kappa * (theta - S) * dt + sigma * np.sqrt(dt) * Z

    pnl_dist = position * (S - S0) / S0
    v95 = -np.percentile(pnl_dist, 5)
    v99 = -np.percentile(pnl_dist, 1)
    return pnl_dist, v95, v99

