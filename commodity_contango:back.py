# ── CORE OU MATH ──────────────────────────────────────────────────────────────
def get_ou_distribution(theta, mu, sigma, s0, tenor):
    T = tenor / 12
    # Mean and Variance for the Log-Price
    mT = mu + (math.log(s0) - mu) * math.exp(-theta * T)
    vT = sigma * math.sqrt((1 - math.exp(-2 * theta * T)) / (2 * theta))

    # Generate price range for x-axis
    prices = np.linspace(max(1, s0 * 0.01), s0 * 3.5, 800)
    lp = np.log(prices)
    pdf = np.exp(-0.5 * ((lp - mT) / vT) ** 2) / (prices * vT * math.sqrt(2 * math.pi))

    mean = math.exp(mT + 0.5 * vT**2)
    p5 = math.exp(mT + norm.ppf(0.05) * vT)
    p95 = math.exp(mT + norm.ppf(0.95) * vT)

    return prices, pdf, mean, p5, p95

