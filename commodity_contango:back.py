conf_levels = [0.95, 0.99]

frames = []
initial_data = None

for vol in vol_grid:
    for conf in conf_levels:

        df, base_var = build_df(vol, conf)

        trace = go.Bar(
            x=df["Scenario"],
            y=df["VaR"],
            marker_color=[SEVERITY_COLOR[s] for s in df["Severity"]],
            text=[f"${v:,.0f}" for v in df["VaR"]],
            textposition="outside"
        )

        frame_name = f"vol={vol:.2f}_conf={conf}"

        frames.append(go.Frame(
            data=[trace],
            name=frame_name
        ))

        if initial_data is None:
            initial_data = [trace]


# ── figure ───────────────────────────────────────────────────────────────────
fig = go.Figure(
    data=initial_data,
    frames=frames
)

# ── sliders ──────────────────────────────────────────────────────────────────
vol_steps = []
for vol in vol_grid:
    vol_steps.append({
        "label": f"{int(vol*100)}%",
        "method": "animate",
        "args": [[f"vol={vol:.2f}_conf=0.99"],
                 {"mode": "immediate"}]
    })

conf_steps = []
for conf in conf_levels:
    conf_steps.append({
        "label": f"{int(conf*100)}%",
        "method": "animate",
        "args": [[f"vol=0.30_conf={conf}"],
                 {"mode": "immediate"}]
    })

fig.update_layout(
    title="⚡ Commodity VaR Scenario Dashboard",
    template="plotly_dark",

    sliders=[
        {
            "active": 0,
            "currentvalue": {"prefix": "Base Vol: "},
            "steps": vol_steps,
            "pad": {"t": 50}
        },
        {
            "active": 1,
            "currentvalue": {"prefix": "Confidence: "},
            "steps": conf_steps,
            "pad": {"t": 120}
        }
    ]
)

# ── axes ─────────────────────────────────────────────────────────────────────
fig.update_yaxes(title_text="Portfolio VaR ($)", tickprefix="$")
fig.update_xaxes(title_text="Scenario", tickangle=-30)

# ── save ─────────────────────────────────────────────────────────────────────
fig.show()

